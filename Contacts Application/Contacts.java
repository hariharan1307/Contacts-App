package com.ContactsApp;

public class Contacts 
{
	 private String Name;
	 private String Number;
	 
	 public Contacts(String Name,String Number)
	 {
		 this.Name=Name;
		 this.Number=Number;
	 }
	 
	 public String getName()
	 {
		 return Name;
	 }
	 public String getNumber()
	 {
		 return Number;
	 }

}
